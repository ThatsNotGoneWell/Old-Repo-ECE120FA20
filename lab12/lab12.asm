
; This program takes input from x5000, x5001 and x5002, 
; and use these information to print a symbol. 
; x5002 decide what symbol it will be printing,
; x5000 and x5001 decide what the font will look like.
; This program first use a loop to find out 
; which symbol should be printed.
; It will find the line and store that address in R2.
; Then it will loop over every first eight bits and 
; print each one of them out using symbols from
; x5000 and x5001. The outer loop, COLUMN, will loop through 
; 16 lines in total. After all the loops,
; the symbol is printed.
; R0 is for printing. R1 is first used in looping 
; to the symbol, then it used to store #16.
; R2 is used to store address of the data.
; R3 is used to store the content of address in R2.
; R4 is used to store #8. 
; R5 and R6 are used to store contents in
; x5000 and x5001. R7 is left blank.

            .ORIG x3000		      ; Program start at x3000
            AND   R0,R0,#0            ; Initialize the register
            AND   R1,R1,#0            ; Initialize the register
            AND   R2,R2,#0            ; Initialize the register
            AND   R3,R3,#0            ; Initialize the register
            AND   R4,R4,#0            ; Initialize the register
            AND   R5,R5,#0            ; Initialize the register
            AND   R6,R6,#0            ; Initialize the register
            AND   R7,R7,#0            ; Initialize the register
            

            LDI   R1,SYMBOL           ; Read content in x5002
            LEA   R2,FONT_DATA        ; Load address of data

LOOP_ONE    ADD   R2,R2,#8            ; Add #16 to R2
            ADD   R2,R2,#8            ;
            ADD   R1,R1,#-1           ;
            BRp   LOOP_ONE            ;

            LDI   R5,SYMBOL_Y         ; Content of x5001 is loaded
            LDI   R6,SYMBOL_N         ; Content of x5000 is loaded


            ADD R1,R1,#8              ; Add #16 to R1
            ADD R1,R1,#8              ;

COLUMN      LD    R0,NEW_L            ;
            OUT                       ;
            ADD   R4,R4,#8            ;
            LDR   R3,R2,#0            ; Load address into R3
ROW         ADD   R3,R3,#0            ; 
            BRn   PRINT_N             ; Jump to print positive
            ADD   R0,R6,#0            ; Print R6
            OUT                       ; Print R6
            BRnzp JUMP                ; Jump after printing
PRINT_N     ADD   R0,R5,#0            ; Print R5
            OUT                       ; Print R5
JUMP        ADD   R3,R3,R3            ; Shift R3 by one bit
            ADD   R4,R4,#-1           ;
            BRp   ROW                 ; Jump to next row
            ADD   R2,R2,#1            ;
            ADD   R1,R1,#-1           ;
            BRp   COLUMN              ; Jump to next column
 

BREAK_C     TRAP x25
SYMBOL     .FILL x5002
SYMBOL_N   .FILL x5000
SYMBOL_Y   .FILL x5001
NEW_L      .FILL x000A


